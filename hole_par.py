import random

# Setting ranges for random time calculations
class HolePar:
    def par_3(self):
        time_range_p3 = random.randint(10, 14)
        return time_range_p3

    def par_4(self):
        time_range_p4 = random.randint(12, 16)
        return time_range_p4

    def par_5(self):
        time_range_p5 = random.randint(16, 20)
        return time_range_p5
